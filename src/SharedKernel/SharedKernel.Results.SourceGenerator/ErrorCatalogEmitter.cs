using System.Text;

namespace SharedKernel.Results.SourceGenerator;

internal static class ErrorCatalogEmitter
{
    public static string Emit(string providerTypeName, IReadOnlyList<ErrorCatalogEntryModel> entries)
    {
        var builder = new StringBuilder();

        builder.AppendLine("// <auto-generated />");
        builder.AppendLine("#nullable enable");
        builder.Append("[assembly: SharedKernel.Results.ResultErrorCatalogProviderAttribute(typeof(SharedKernel.Results.")
            .Append(providerTypeName)
            .AppendLine("))]");
        builder.AppendLine();
        builder.AppendLine("namespace SharedKernel.Results;");
        builder.AppendLine();
        builder.AppendLine("internal static class ResultErrorCatalogGenerated");
        builder.AppendLine("{");
        builder.AppendLine("    internal static readonly ResultErrorCatalogEntry[] generatedEntries =");
        builder.AppendLine("    [");

        foreach (var entry in entries)
        {
            builder.AppendLine("        new ResultErrorCatalogEntry");
            builder.AppendLine("        {");
            builder.Append("            Identifier = ").Append(ToLiteral(entry.Identifier)).AppendLine(",");
            builder.Append("            DocumentationPath = ").Append(ToLiteral(entry.DocumentationPath)).AppendLine(",");
            builder.Append("            ProviderType = ").Append(ToLiteral(entry.ProviderType)).AppendLine(",");
            builder.Append("            MemberName = ").Append(ToLiteral(entry.MemberName)).AppendLine(",");
            builder.Append("            Status = ResultStatus.").Append(entry.Status).AppendLine(",");
            builder.Append("            Code = ").Append(ToLiteral(entry.Code)).AppendLine(",");
            builder.Append("            DetailTemplate = ").Append(ToLiteral(entry.DetailTemplate)).AppendLine(",");
            builder.Append("            Summary = ").Append(entry.Summary is null ? "null" : ToLiteral(entry.Summary)).AppendLine();
            builder.AppendLine("        },");
        }

        builder.AppendLine("    ];");
        builder.AppendLine("}");
        builder.AppendLine();
        builder.AppendLine("/// <summary>");
        builder.AppendLine("/// Provides the generated centralized error-catalog entries for this assembly.");
        builder.AppendLine("/// </summary>");
        builder.Append("public sealed class ")
            .Append(providerTypeName)
            .AppendLine();
        builder.AppendLine("{");
        builder.AppendLine("    /// <summary>");
        builder.AppendLine("    /// Gets the generated error-catalog entries for this assembly.");
        builder.AppendLine("    /// </summary>");
        builder.AppendLine("    public global::System.Collections.Generic.IReadOnlyList<ResultErrorCatalogEntry> Entries => ResultErrorCatalogGenerated.generatedEntries;");
        builder.AppendLine("}");

        return builder.ToString();
    }

    private static string ToLiteral(string value)
    {
        return "\""
            + value
                .Replace("\\", "\\\\")
                .Replace("\"", "\\\"")
                .Replace("\r", "\\r")
                .Replace("\n", "\\n")
            + "\"";
    }
}
