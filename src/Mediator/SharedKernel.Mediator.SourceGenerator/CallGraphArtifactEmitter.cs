using System.Text;

namespace SharedKernel.Mediator.SourceGenerator;

/// <summary>
/// Emits an optional JSON call-graph artifact for the current mediator compilation.
/// </summary>
internal static class CallGraphArtifactEmitter
{
    public static string Emit(DiscoveryModel model)
    {
        var builder = new StringBuilder();
        GeneratedXmlDocumentationEmitter.AppendAutoGeneratedHeader(builder);
        builder.AppendLine("namespace SharedKernel.Mediator.Generated;");
        builder.AppendLine();
        GeneratedXmlDocumentationEmitter.AppendSummary(
            builder,
            string.Empty,
            "Provides the optional generated JSON call graph for the current mediator compilation.");
        builder.AppendLine("internal static class MediatorCallGraph");
        builder.AppendLine("{");
        builder.AppendLine("    public static string Json { get; } =");
        builder.AppendLine("        \"\"\"");

        foreach (var line in BuildJson(model).Split('\n'))
        {
            builder.Append("        ").AppendLine(line);
        }

        builder.AppendLine("        \"\"\";");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static string BuildJson(DiscoveryModel model)
    {
        var builder = new StringBuilder();
        builder.AppendLine("{");
        AppendNamedObjectArray(
            builder,
            "requests",
            model.Requests.Select(CreateRequestObject));
        builder.AppendLine(",");
        AppendNamedObjectArray(
            builder,
            "notifications",
            model.Notifications.Select(CreateNotificationObject));
        builder.AppendLine(",");
        AppendNamedObjectArray(
            builder,
            "streamRequests",
            model.StreamRequests.Select(CreateStreamRequestObject));
        builder.AppendLine();
        builder.Append('}');
        return builder.ToString();
    }

    private static string CreateRequestObject(RequestDescriptor request)
    {
        var builder = new StringBuilder();
        builder.AppendLine("    {");
        AppendProperty(builder, "request", request.MetadataName, trailingComma: true);
        AppendProperty(builder, "response", request.Response.MetadataName, trailingComma: true);
        AppendProperty(builder, "handler", request.Handlers.Length == 1 ? request.Handlers[0].MetadataName : "<none>", trailingComma: true);
        AppendNamedStringArray(builder, "pipelines", request.Pipelines.Select(static pipeline => pipeline.MetadataName), trailingComma: true);
        AppendProperty(builder, "aot", "fully-generated", trailingComma: false);
        builder.Append("    }");
        return builder.ToString();
    }

    private static string CreateNotificationObject(NotificationDescriptor notification)
    {
        var builder = new StringBuilder();
        builder.AppendLine("    {");
        AppendProperty(builder, "notification", notification.MetadataName, trailingComma: true);
        AppendNamedStringArray(builder, "handlers", notification.Handlers.Select(static handler => handler.MetadataName), trailingComma: true);
        AppendProperty(builder, "dispatch", notification.PublishInParallel ? "parallel" : "sequential", trailingComma: false);
        builder.Append("    }");
        return builder.ToString();
    }

    private static string CreateStreamRequestObject(StreamRequestDescriptor streamRequest)
    {
        var builder = new StringBuilder();
        builder.AppendLine("    {");
        AppendProperty(builder, "request", streamRequest.MetadataName, trailingComma: true);
        AppendProperty(builder, "response", streamRequest.ItemResponse.MetadataName, trailingComma: true);
        AppendProperty(builder, "handler", streamRequest.Handlers.Length == 1 ? streamRequest.Handlers[0].MetadataName : "<none>", trailingComma: true);
        AppendNamedStringArray(builder, "pipelines", streamRequest.Pipelines.Select(static pipeline => pipeline.MetadataName), trailingComma: true);
        AppendProperty(builder, "aot", "fully-generated", trailingComma: false);
        builder.Append("    }");
        return builder.ToString();
    }

    private static void AppendNamedObjectArray(StringBuilder builder, string propertyName, IEnumerable<string> objects)
    {
        builder.Append("  \"").Append(propertyName).AppendLine("\": [");
        var values = objects.ToArray();

        for (var index = 0; index < values.Length; index++)
        {
            builder.Append(values[index]);
            if (index < values.Length - 1)
            {
                builder.Append(',');
            }

            builder.AppendLine();
        }

        builder.Append("  ]");
    }

    private static void AppendNamedStringArray(StringBuilder builder, string propertyName, IEnumerable<string> values, bool trailingComma)
    {
        builder.Append("      \"").Append(propertyName).AppendLine("\": [");
        var entries = values.ToArray();

        for (var index = 0; index < entries.Length; index++)
        {
            builder.Append("        ").Append(MediatorGenerationNames.EscapeStringLiteral(entries[index]));
            if (index < entries.Length - 1)
            {
                builder.Append(',');
            }

            builder.AppendLine();
        }

        builder.Append("      ]");
        if (trailingComma)
        {
            builder.Append(',');
        }

        builder.AppendLine();
    }

    private static void AppendProperty(StringBuilder builder, string propertyName, string value, bool trailingComma)
    {
        builder.Append("      \"")
            .Append(propertyName)
            .Append("\": ")
            .Append(MediatorGenerationNames.EscapeStringLiteral(value));
        if (trailingComma)
        {
            builder.Append(',');
        }

        builder.AppendLine();
    }

}
