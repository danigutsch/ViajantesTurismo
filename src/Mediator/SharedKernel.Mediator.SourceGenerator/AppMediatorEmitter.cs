using System.Text;

namespace SharedKernel.Mediator.SourceGenerator;

/// <summary>
/// Emits the generated mediator shell owned by the consumer compilation.
/// </summary>
internal static class AppMediatorEmitter
{
    public static string Emit(DiscoveryModel model)
    {
        var builder = new StringBuilder();
        GeneratedXmlDocumentationEmitter.AppendAutoGeneratedHeader(builder);
        builder.AppendLine("namespace SharedKernel.Mediator;");
        builder.AppendLine();
        GeneratedXmlDocumentationEmitter.AppendSummary(
            builder,
            string.Empty,
            "Provides the generated mediator shell owned by the consumer compilation.");
        builder.AppendLine("public sealed partial class AppMediator : IMediator");
        builder.AppendLine("{");
        GeneratedXmlDocumentationEmitter.AppendSummary(
            builder,
            "    ",
            "Initializes a new instance of the <see cref=\"AppMediator\"/> class.");
        GeneratedXmlDocumentationEmitter.AppendParam(
            builder,
            "    ",
            "services",
            "The scoped service provider used by generated mediator code.");
        builder.AppendLine("    public AppMediator(global::System.IServiceProvider services)");
        builder.AppendLine("    {");
        builder.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(services);");
        builder.AppendLine();
        builder.AppendLine("        Services = services;");
        builder.AppendLine("    }");
        builder.AppendLine();
        GeneratedXmlDocumentationEmitter.AppendSummary(
            builder,
            "    ",
            "Gets the scoped service provider used by generated mediator code.");
        builder.AppendLine("    internal global::System.IServiceProvider Services { get; }");
        builder.AppendLine();

        foreach (var request in model.Requests)
        {
            EmitTypedSendOverload(builder, request);
        }

        builder.AppendLine();
        GeneratedXmlDocumentationEmitter.AppendInheritdoc(builder, "    ");
        builder.AppendLine("    public global::System.Threading.Tasks.ValueTask<TResponse> Send<TResponse>(");
        builder.AppendLine("        global::SharedKernel.Mediator.IRequest<TResponse> request,");
