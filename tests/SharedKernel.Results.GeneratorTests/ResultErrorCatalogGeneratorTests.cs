namespace SharedKernel.Results.GeneratorTests;

public sealed class ResultErrorCatalogGeneratorTests
{
    [Fact]
    public void Generates_error_catalog_for_static_error_provider_methods()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class UserErrors
            {
                public static Result NotFound(Guid id) => Result.NotFound($"User with id {id} was not found.");

                public static Result DuplicateEmail(string email) => Result.Conflict($"User with email '{email}' already exists.");

                public static Result InvalidEmail() => Result.Invalid(
                    detail: "Email is not valid.",
                    field: "Email",
                    message: "Email is invalid.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("// <auto-generated />", generatedSource, StringComparison.Ordinal);
        Assert.Contains("[assembly: SharedKernel.Results.ResultErrorCatalogProviderAttribute(typeof(SharedKernel.Results.", generatedSource, StringComparison.Ordinal);
        Assert.Contains("internal static class ResultErrorCatalogGenerated", generatedSource, StringComparison.Ordinal);
        Assert.Contains("public sealed class Generated", generatedSource, StringComparison.Ordinal);
        Assert.Contains("demo-usererrors-notfound", generatedSource, StringComparison.Ordinal);
        Assert.Contains("demo-usererrors-duplicateemail", generatedSource, StringComparison.Ordinal);
        Assert.Contains("demo-usererrors-invalidemail", generatedSource, StringComparison.Ordinal);
        Assert.Contains("docs/errors/README.md", generatedSource, StringComparison.Ordinal);
        Assert.Contains("ResultStatus.NotFound", generatedSource, StringComparison.Ordinal);
        Assert.Contains("ResultStatus.Conflict", generatedSource, StringComparison.Ordinal);
        Assert.Contains("ResultStatus.Invalid", generatedSource, StringComparison.Ordinal);
        Assert.Contains("\"not_found\"", generatedSource, StringComparison.Ordinal);
        Assert.Contains("\"conflict\"", generatedSource, StringComparison.Ordinal);
        Assert.Contains("\"invalid\"", generatedSource, StringComparison.Ordinal);
        Assert.Contains("User with id {id} was not found.", generatedSource, StringComparison.Ordinal);
        Assert.Contains("User with email '{email}' already exists.", generatedSource, StringComparison.Ordinal);
        Assert.Contains("Email is not valid.", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Generates_error_catalog_for_result_error_factory_without_http_metadata()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class UserErrors
            {
                public static Result SaveFailed() => Result.Error("Save failed.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("ResultStatus.Error", generatedSource, StringComparison.Ordinal);
        Assert.DoesNotContain("HttpStatusCode", generatedSource, StringComparison.Ordinal);
        Assert.Contains("Code = \"error\"", generatedSource, StringComparison.Ordinal);
        Assert.Contains("DetailTemplate = \"Save failed.\"", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Ignores_non_error_classes_and_non_result_members()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class UserMessages
            {
                public static string Greeting() => "hello";
            }

            public static class UserErrors
            {
                public static string Greeting() => "hello";
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        // Assert
        Assert.Empty(runResult.Results.Single().GeneratedSources);
    }

    [Fact]
    public void Generates_error_catalog_for_internal_error_providers_returning_generic_results()
    {
        // Arrange
        const string source = """
            namespace Demo;

            internal static class CsvErrors
            {
                internal static Result<string> MissingHeader(string columnName) => Result.Invalid<string>(
                    detail: $"Required column '{columnName}' is missing.",
                    field: "Headers",
                    message: "Required column is missing.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("demo-csverrors-missingheader", generatedSource, StringComparison.Ordinal);
        Assert.Contains("ResultStatus.Invalid", generatedSource, StringComparison.Ordinal);
        Assert.Contains("\"invalid\"", generatedSource, StringComparison.Ordinal);
        Assert.Contains("Required column '{columnName}' is missing.", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Generates_error_catalog_for_static_properties()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class SystemErrors
            {
                public static Result DatabaseUnavailable => Result.Unavailable("Database is unavailable.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("demo-systemerrors-databaseunavailable", generatedSource, StringComparison.Ordinal);
        Assert.Contains("ResultStatus.Unavailable", generatedSource, StringComparison.Ordinal);
        Assert.Contains("Database is unavailable.", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Adds_parameter_type_suffix_to_overloaded_method_identifiers()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class UserErrors
            {
                public static Result NotFound(Guid id) => Result.NotFound($"User {id} was not found.");

                public static Result NotFound(string email) => Result.NotFound($"User {email} was not found.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("demo-usererrors-notfound-guid", generatedSource, StringComparison.Ordinal);
        Assert.Contains("demo-usererrors-notfound-string", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Preserves_interpolated_template_format_clauses()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class TourErrors
            {
                public static Result StartDateInPast(DateTime startDate) =>
                    Result.Invalid($"Start date {startDate:yyyy-MM-dd} cannot be in the past.", "StartDate", "Start date is invalid.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("Start date {startDate:yyyy-MM-dd} cannot be in the past.", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Escapes_generated_string_literals()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class FileErrors
            {
                public static Result InvalidPath() => Result.Invalid(
                    "Path \"C:\\\\Temp\" is invalid.\nChoose another path.",
                    "Path",
                    "Path is invalid.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);
        var generatedSource = GeneratorTestHarness.GetGeneratedSource(runResult);

        // Assert
        Assert.Contains("Path \\\"C:\\\\\\\\Temp\\\" is invalid.\\nChoose another path.", generatedSource, StringComparison.Ordinal);
    }

    [Fact]
    public void Ignores_unsupported_result_member_shapes()
    {
        // Arrange
        const string source = """
            namespace Demo;

            public static class UserErrors
            {
                public static Result MissingExpressionBody()
                {
                    var detail = "User was not found.";
                    return Result.NotFound(detail);
                }

                public static Result Success() => Result.Success();

                public static Result Wrapped() => CreateNotFound();

                private static Result CreateNotFound() => Result.NotFound("User was not found.");
            }
            """;
        var compilation = GeneratorTestHarness.CreateCompilation(source);

        // Act
        var runResult = GeneratorTestHarness.RunGeneratorDriver(compilation);

        // Assert
        Assert.Empty(runResult.Results.Single().GeneratedSources);
    }
}
