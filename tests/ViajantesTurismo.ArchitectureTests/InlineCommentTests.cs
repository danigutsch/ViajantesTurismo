using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;

namespace ViajantesTurismo.ArchitectureTests;

public sealed class InlineCommentTests
{
    private static readonly string SolutionRoot = Path.GetFullPath(Path.Combine(AppContext.BaseDirectory, "..", "..", "..", "..", ".."));
    private static readonly string SourceRoot = Path.Combine(SolutionRoot, "src");

    private static readonly string[] ExcludedDirectories =
    [
        "obj",
        "bin",
        "Migrations"
    ];

    private static readonly string[] GeneratedFileSuffixes =
    [
        ".g.cs",
        ".generated.cs",
        ".designer.cs"
    ];

    private static readonly string[] AllowedCommentPrefixes =
    [
        "// <auto-generated",
        "// ReSharper disable",
        "// ReSharper restore"
    ];

    [Fact]
    public void SourceFiles_Should_Not_Contain_InlineComments()
    {
        if (!Directory.Exists(SourceRoot))
        {
            throw new DirectoryNotFoundException($"Could not locate source root at '{SourceRoot}'.");
        }

        var offendingComments = new List<string>();

        foreach (var filePath in Directory.EnumerateFiles(SourceRoot, "*.cs", SearchOption.AllDirectories))
        {
            if (ShouldSkipFile(filePath))
            {
                continue;
            }

            var fileText = File.ReadAllText(filePath);
            if (ContainsAutoGeneratedMarker(fileText))
            {
                continue;
            }

            var syntaxTree = CSharpSyntaxTree.ParseText(fileText, cancellationToken: TestContext.Current.CancellationToken);
            var root = syntaxTree.GetRoot(TestContext.Current.CancellationToken);

            var inlineComments = root
                .DescendantTrivia(descendIntoTrivia: true)
                .Where(trivia => trivia.IsKind(SyntaxKind.SingleLineCommentTrivia))
                .Where(trivia => !IsAllowedComment(trivia));

            foreach (var comment in inlineComments)
            {
                var lineSpan = comment.GetLocation().GetLineSpan();
                var lineNumber = lineSpan.StartLinePosition.Line + 1;
                var relativePath = Path.GetRelativePath(SolutionRoot, filePath);
                var commentPreview = comment.ToFullString().Trim();
                offendingComments.Add($"{relativePath} (line {lineNumber}): {commentPreview}");
            }
        }

        Assert.False(
            offendingComments.Count != 0,
            $"Inline comments are not allowed. Remove or refactor the following occurrences:{Environment.NewLine}{string.Join(Environment.NewLine, offendingComments)}");
    }

    private static bool ShouldSkipFile(string filePath)
    {
        if (GeneratedFileSuffixes.Any(suffix => filePath.EndsWith(suffix, StringComparison.OrdinalIgnoreCase)))
        {
            return true;
        }

        var directories = filePath.Split(Path.DirectorySeparatorChar, Path.AltDirectorySeparatorChar);
        if (directories.Any(segment => ExcludedDirectories.Contains(segment, StringComparer.OrdinalIgnoreCase)))
        {
            return true;
        }

        return false;
    }

    private static bool ContainsAutoGeneratedMarker(string fileText) =>
        fileText.Contains("<auto-generated", StringComparison.OrdinalIgnoreCase);

    private static bool IsAllowedComment(SyntaxTrivia trivia)
    {
        var commentText = trivia.ToFullString().Trim();
        return AllowedCommentPrefixes.Any(prefix => commentText.StartsWith(prefix, StringComparison.Ordinal));
    }
}